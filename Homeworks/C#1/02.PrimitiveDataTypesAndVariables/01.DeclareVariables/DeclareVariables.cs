using System;

class DeclareVariables
{
    static void Main(string[] args)
    {
        ushort a = 52130;
        sbyte b = -115;
        int c = 4825932;
        byte d = 97;
        short e = -10000;

        Console.WriteLine("ushort variable: " + a
                                + "; \nsbyte variable: "
                                + b + "; \nint variable: "
                                + c + "; \nbyte variable: "
                                + d + "; \nshort variable: "
                                + e);
    }
}

